package entity;

public class Coordinate {
    
    Integer x;
    Integer y;

    public void setX ( Integer x ) {
        this.x = x;
    }

    public Integer getX () {
        return this.x;
    }
    
    public void setY ( Integer y ) {
        this.y = y;
    }
    
    public Integer getY () {
        return this.y;
    }

}
